export function getWeapon(value: string): WeaponName {
  const enumKey = Object.keys(WeaponName).find(
    (key) => WeaponName[key as keyof typeof WeaponName] === value
  )
  if (enumKey !== undefined) {
    return WeaponName[enumKey as keyof typeof WeaponName]
  }
  return WeaponName.UNKNOWN
}
